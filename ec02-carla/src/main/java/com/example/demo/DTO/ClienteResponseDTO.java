package com.example.demo.DTO;

public class ClienteResponseDTO {
	
	private Integer idClienterp;
	private String nombreClienterp;
	private String direccionClienterp;
	private String dniClienterp;
	
	public Integer getIdClienterp() {
		return idClienterp;
	}
	public void setIdClienterp(Integer idClienterp) {
		this.idClienterp = idClienterp;
	}
	public String getNombreClienterp() {
		return nombreClienterp;
	}
	public void setNombreClienterp(String nombreClienterp) {
		this.nombreClienterp = nombreClienterp;
	}
	public String getDireccionClienterp() {
		return direccionClienterp;
	}
	public void setDireccionClienterp(String direccionClienterp) {
		this.direccionClienterp = direccionClienterp;
	}
	public String getDniClienterp() {
		return dniClienterp;
	}
	public void setDniClienterp(String dniClienterp) {
		this.dniClienterp = dniClienterp;
	}
	
	
	
}
